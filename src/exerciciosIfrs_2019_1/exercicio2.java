package exerciciosIfrs_2019_1;

public class exercicio2 {

	/*Escreva um programa em Java que, dadas duas variáveis, a e b, sendo que a
= 5 e b = 9, troque e exiba os valores de a e b, ou seja, a deverá a valer 9 e b
deverá valer 5. */
	
	public static void main(String [] args) {
		
		int a = 5;
		int b = 9;
		int somaA = a + 4;
		int dimB = b - 4;
		
		System.out.println("O valor inicial de A é de " + a + " e B vale " + b + "!");
	
		System.out.println ("Mas se trocarmos o valor de A e B... A valerá " + somaA + " e B valerá " + dimB + "!");
	}
}
